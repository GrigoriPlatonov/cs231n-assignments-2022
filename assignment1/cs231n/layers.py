def svm_loss(x, y):
    """
    Computes the loss and gradient using for multiclass SVM classification.

    Inputs:
    - x: Input data, of shape (N, C) where x[i, j] is the score for the jth
      class for the ith input.
    - y: Vector of labels, of shape (N,) where y[i] is the label for x[i] and
      0 <= y[i] < C

    Returns a tuple of:
    - loss: Scalar giving the loss
    - dx: Gradient of the loss with respect to x
    """
    loss, dx = None, None
    ###########################################################################
    # TODO: Implement loss and gradient for multiclass SVM classification.    #
    # This will be similar to the svm loss vectorized implementation in       #
    # cs231n/classifiers/linear_svm.py.                                       #
    ###########################################################################
    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****

    # pass
    num_train = x.shape[0]
    num_classes = x.shape[1]
    scores = x
    # labelScores = np.reshape(np.repeat(scores[range(num_train),y], num_classes), scores.shape)
    labelScores = scores[range(num_train),y][:, np.newaxis]
    margins = np.maximum(scores - labelScores + 1, 0)
    margins[range(num_train),y] = 0
    loss = np.sum(margins) / num_train

    mask = (margins > 0).astype(int)
    mask[range(num_train),y] -= np.sum(mask,axis=1)
    dx = mask / num_train


    # *****END OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    ###########################################################################
    #                             END OF YOUR CODE                            #
    ###########################################################################
    return loss, dx


def softmax_loss(x, y):
    """
    Computes the loss and gradient for softmax classification.

    Inputs:
    - x: Input data, of shape (N, C) where x[i, j] is the score for the jth
      class for the ith input.
    - y: Vector of labels, of shape (N,) where y[i] is the label for x[i] and
      0 <= y[i] < C

    Returns a tuple of:
    - loss: Scalar giving the loss
    - dx: Gradient of the loss with respect to x
    """
    loss, dx = None, None
    ###########################################################################
    # TODO: Implement the loss and gradient for softmax classification. This  #
    # will be similar to the softmax loss vectorized implementation in        #
    # cs231n/classifiers/softmax.py.                                          #
    ###########################################################################
    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****

    # pass
    num_train = x.shape[0]
    num_classes = x.shape[1]
    y_hat = x
    y_exp = np.exp(y_hat - y_hat.max())
    y_sum = np.sum(y_exp, axis=1, keepdims=True) # the result has the same row numbers as before
    # softmax = y_exp / np.reshape(np.repeat(y_sum, num_classes), y_exp.shape)
    softmax = y_exp / y_sum 
    loss = np.sum(-np.log(softmax[np.arange(num_train),y]))
    loss /= num_train

    softmax[np.arange(num_train),y] -= 1
    dx = softmax / num_train


    # *****END OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    ###########################################################################
    #                             END OF YOUR CODE                            #
    ###########################################################################
    return loss, dx
